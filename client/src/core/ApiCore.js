import { API } from "../config";

export const getEvents = (sortBy) => {
  return fetch(`${API}/events?sortBy=${sortBy}&limit=undefined`, {
    method: "GET",
  })
    .then((response) => {
      return response.json();
    })
    .catch((err) => console.log(err));
};

